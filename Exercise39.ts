function city_country(city:string, country:string){

    return `${city}, ${country}`;
}

let cityCountry_1 = city_country("Lahore", "Pakistan");
let cityCountry_2 = city_country("Stockholm", "Sweden");
let cityCountry_3 = city_country("London", "UK");

console.log(cityCountry_1);
console.log(cityCountry_2);
console.log(cityCountry_3);